#include "libft.h"

void		ft_tabdel(char **t)
{
	int		i;

	i = -1;
	if (t != NULL)
	{
		while (t[++i] != NULL)
			ft_strdel(&(t[i]));
		free((char **)t);
		t = NULL;
	}
}
