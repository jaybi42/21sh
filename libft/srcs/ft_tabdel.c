#include "libft.h"

void		ft_tabdel(char **tab2)
{
	int	i;

	i = (-1);
	if (tab2 != NULL)
	{
		while (tab2[++i] != NULL)
			ft_strdel(&(tab2[i]));
		free((char **)tab2);
		tab2 = NULL;
	}
}
